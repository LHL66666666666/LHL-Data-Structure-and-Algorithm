//删除至少几个字符可以变成另一个字符串的子串
//给定两个字符串s1,s2,
//s1长度为n,s2长度为m
//s1至少删除几个字符可以变成s2的子串
//子串要求连续
//也就是说，要求s1的子序列是s2的子串
//关键在于状态的设计
//定义dp[i][j]
//表示s1前i个字符的前缀要变成s2取前j个字符字符的前缀串的某一个后缀，至少删掉几个字符
//基于s2要求子串来思考，枚举每个子串的结束位置
//状态转移：dp[i][j]时：
//如果s1[i-1]==s2[j-1]这个位置的字符不需要删除，dp[i][j]=dp[i-1][j-1]
//如果s1[i-1]!=s2[j-1]这个位置的字符需要删除，dp[i][j]=1+dp[i-1][j]
//答案取第n行的最小值即可，意思是s1删除几个字符能变成s2以j位置结尾的子串
//取最小值
//基础情况：i==0时，答案为0，也就是不用删就是后缀串
//j==0时，dp[i][0]=i，要全删掉才是后缀串