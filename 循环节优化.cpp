//给定一个整数n(1<=n<=100000000)
//求n的n次幂的个位数
#include <iostream>
using namespace std;

int lastDigitOfPower(int n) {
    int base = n % 10;
    int exponent = n;

    // 所有数字的循环节
    int cycles[10][4] = {
        {0},           // 0
        {1},           // 1
        {2,4,8,6},     // 2
        {3,9,7,1},     // 3
        {4,6},         // 4
        {5},           // 5
        {6},           // 6
        {7,9,3,1},     // 7
        {8,4,2,6},     // 8
        {9,1}          // 9
    };

    int cycleLength[10] = { 1,1,4,4,2,1,1,4,4,2 };

    // 如果循环节长度为1，直接返回
    if (cycleLength[base] == 1) {
        return cycles[base][0];
    }

    // 计算在循环节中的位置
    int pos = (exponent - 1) % cycleLength[base];
    return cycles[base][pos];
}

int main() {
    int n;
    cout << "请输入整数n: ";
    cin >> n;

    int result = lastDigitOfPower(n);
    cout << n << "^" << n << "的个位数是: " << result << endl;

    return 0;
}
//算法说明：
//
//循环节原理：每个数字的幂次个位数都会形成循环
//
//0: 0, 0, 0, ... → 循环节[0]，长度1
//
//1 : 1, 1, 1, ... → 循环节[1]，长度1
//
//2 : 2, 4, 8, 6, 2, ... → 循环节[2, 4, 8, 6]，长度4
//
//3 : 3, 9, 7, 1, 3, ... → 循环节[3, 9, 7, 1]，长度4
//
//4 : 4, 6, 4, ... → 循环节[4, 6]，长度2
//
//5 : 5, 5, 5, ... → 循环节[5]，长度1
//
//6 : 6, 6, 6, ... → 循环节[6]，长度1
//
//7 : 7, 9, 3, 1, 7, ... → 循环节[7, 9, 3, 1]，长度4
//
//8 : 8, 4, 2, 6, 8, ... → 循环节[8, 4, 2, 6]，长度4
//
//9 : 9, 1, 9, ... → 循环节[9, 1]，长度2
//
//时间复杂度：O(1)，因为循环节长度最多为4
//
//空间复杂度：O(1)，只需要存储循环节数据