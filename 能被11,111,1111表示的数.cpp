//给定一个整数n，判断这个数字能否被
//11，111，1111，11111...中的数字经过多次相加得到
//（数字可以重复使用）
//例如：33=11+11+11，144=11+11+11+111
//输入一行一个正整数n，n<=10^9
//如果可以，输出yes，不能，输出no
#include <iostream>
using namespace std;
int main() {
	int n;
	cin >> n;
	for (int b = 0; b * 111 <= n; b++)
	{
		int tmp = n - b * 111;
		if (tmp % 11 == 0 && tmp>=0)
		{
			cout << "tes" << endl;
			return 0;
		}
	}
	cout << "no" << endl;
	
	return 0;
}
/*
核心洞察
虽然题目允许使用11, 111, 1111, 11111,...等所有由多个1组成的数字，
但实际上只需要11和111就足够了。

为什么？

1111 = 101 × 11

11111 = 1010 × 11 + 1 = 1000*11+111

111111 = 10101 × 11

所有更大的由1组成的数字都可以用11的倍数加上0或1来表示，
而这些"余数1"的效果可以用111来替代。
因为111=11*10+1
事实上也可以这样理解
如果有奇数个1，那么肯定能表示成111+11的倍数
如果有偶数个1，那么就是11的倍数
所以这些数字都能被11和111表示
也就是说我们只需要找能被11和111的倍数表示的即可
我们要判断n是否能表示为：
n = 11a + 111b （其中a,b为非负整数）
*/