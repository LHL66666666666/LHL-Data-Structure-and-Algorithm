//有效涂色问题
//给定参数n,m
//一共有n个格子，每个格子可以涂上一种颜色，颜色在m种中选择
//当n个格子都被颜色涂满，且m种颜色都已经使用，称为一种有效方法
//求一共有多少种有效涂色方法
//数据保证:
//1<=n,m<=5000
//由于答案很大，对1e9+7取模之后返回
//状态定义：dp[i][j]为使用j种颜色按要求填满i个格子的方法数
//状态转移：考虑格子数为i-1的状态
//1）假设i-1时已经满足j种颜色，第i个格子随意填，从j种中选一个即可，为j*dp[i-1][j]
//2）假设i-1时已经满足j-1种颜色，需要第i个格子补满第j种颜色，第i个格子从剩余的m-(j-1)种颜色中选即可
//2）如果i-1时颜色小于等于j-2，由于加上一个新的格子最多加一种新颜色，加上一个格子无法满足要求
//所以1），2）就是全部情况
//实现方面很简单，第1行，第1列是基础情况，从2行2列开始填即可
//dp[i][j]=j*dp[i-1][j]+(m-j+1)*dp[i-1][j-1]